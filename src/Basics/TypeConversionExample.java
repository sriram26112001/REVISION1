package Basics;

public class TypeConversionExample {
    public static void main(String[] args) {
        //Type conversion, also known as type casting or type coercion, is the process of converting a variable from one data type to another. This can be done explicitly by the programmer or implicitly by the compiler or interpreter.
        //Type conversion
        int a=5;
        float b=2.5f;
        System.out.println(a+b);
    }
}
